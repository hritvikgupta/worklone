def create_tool(tool_name: str) -> Optional[BaseTool]:
    """Instantiate a tool from either its UI label or runtime name."""
    if not tool_name:
        return None
    factory = TOOL_CATALOG.get(tool_name.strip().lower())
    if not factory:
        return None
    return factory()


def create_all_tools() -> list[BaseTool]:
    """Instantiate each runtime tool exactly once.

    Provider-root tools (GmailTool, SlackTool, NotionTool, GitHubTool, JiraTool)
    are assignment bundles / compatibility aliases and are not included in the
    normal runtime registry. Agents should use the modular provider tools.
    """
    tools: list[BaseTool] = []
    seen: set[str] = set()
    bundle_root_classes = set(PROVIDER_TOOL_BUNDLES.keys())

    for tool_meta in list_catalog_tools():
        if tool_meta["name"] in bundle_root_classes:
            continue
        tool = create_tool(tool_meta["name"])
        if not tool or tool.name in seen:
            continue
        tools.append(tool)
        seen.add(tool.name)

    return tools
