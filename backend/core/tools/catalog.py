def list_assignable_employee_tools() -> list[dict]:
    """Return employee tool choices for UI assignment.

    Provider integrations are exposed as bundle roots so selecting Gmail/Slack/etc.
    gives the employee the full provider capability set automatically.
    """
    assignable: list[dict] = []
    seen: set[str] = set()

    # Emit one entry per provider bundle
    for root_name, bundle in PROVIDER_TOOL_BUNDLES.items():
        if root_name in seen:
            continue
        seen.add(root_name)
        label = bundle.get("label", root_name)
        assignable.append({
            "name": root_name,
            "runtime_name": root_name,
            "description": _BUNDLE_DESCRIPTION.get(root_name, f"Full {label} integration bundle."),
            "category": _BUNDLE_CATEGORY.get(root_name, "integration"),
            "is_optional": True,
            "is_default": False,
        })

    # Also include non-bundle optional tools (e.g. workflow tools, analytics)
    for tool in list_catalog_tools():
        name = tool["name"]
        runtime_name = tool["runtime_name"]
        if not tool["is_optional"]:
            continue
        if name in seen:
            continue
        # Skip individual bundle members — already covered by the root above
        if _BUNDLE_ALIAS_TO_ROOT.get(name.lower()) or _BUNDLE_ALIAS_TO_ROOT.get(runtime_name.lower()):
            continue
        seen.add(name)
        assignable.append(tool)

    assignable.sort(key=lambda item: (item["category"], item["name"]))
    return assignable


def expand_tool_selection(tool_names: list[str]) -> list[str]:
    """Expand provider-root selections to all runtime tools in that provider bundle."""
    expanded: list[str] = []
    seen: set[str] = set()

    for raw_name in tool_names:
        normalized = (raw_name or "").strip()
        if not normalized:
            continue

        root = _BUNDLE_ALIAS_TO_ROOT.get(normalized.lower())
        if root and root in PROVIDER_TOOL_BUNDLES:
            for runtime_name in sorted(PROVIDER_TOOL_BUNDLES[root]["members"]):
                if runtime_name not in seen:
                    expanded.append(runtime_name)
                    seen.add(runtime_name)
            continue

        if normalized not in seen:
            expanded.append(normalized)
            seen.add(normalized)

    return expanded


